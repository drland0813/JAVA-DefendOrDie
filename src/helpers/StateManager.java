	public static void update() {
		switch (gameState) {
		case MAIN_MENU:
			if (mainMenu == null)
				mainMenu = new MainMenu();
			mainMenu.update();
			break;
		case GAME:
			if (game == null)
				game = new Game(map1);
			game.update();
			break;
		case BETA:
			if (gamebeta == null)
				gamebeta = new GameNewMode(mapBeta);
			gamebeta.update();
			break;
		case RESUME:
			setPause(false);
			game.update();
			break;
		case SELECTMAP:
			game = null;
			if (mapMenu == null) {
				mapMenu = new MapMenu();
			}

			mapMenu.update();
			break;
		case MAP1:
			setPause(false);
			if (game == null)
				game = new Game(map1, 1);
			game.update();
			break;
		case MAP2:
			setPause(false);
			if (game == null)
				game = new Game(map2, 2);
			game.update();
			break;
		case MAP3:
			setPause(false);
			if (game == null)
				game = new Game(map3, 3);
			game.update();
			break;
		case MAP4:
			setPause(false);
			if (game == null)
				game = new Game(map4, 4);
			game.update();
			break;
		case MAP5:
			setPause(false);
			if (game == null)
				game = new Game(map5, 5);
			game.update();
			break;
		case MAP6:
			setPause(false);
			if (game == null)
				game = new Game(map6, 6);
			game.update();
			break;
		case MAP7:
			setPause(false);
			if (game == null)
				game = new Game(map7, 7);
			game.update();
			break;
		case PAUSE:
			if (pausePlayMenu == null) {
				Pause();
				pausePlayMenu = new PausePlayMenu();
			}
			pausePlayMenu.update();
			break;
		case VICTORY:
			if (victoryMenu == null) {
				victoryMenu = new VictoryMenu();
			}
			victoryMenu.update();
			break;
		case DEFEAT:
			if (defeatMenu == null) {
				defeatMenu = new DefeatMenu();
			}
			defeatMenu.update();
			break;
		}

		long currentTime = System.currentTimeMillis();
		if (currentTime > nextSecond) {
			nextSecond += 1000;
			frameInLastSecond = frameInCurrentSecond;
			frameInCurrentSecond = 0;
		}
		frameInCurrentSecond++;
	}

	public static void setState(GameState newState) {
		gameState = newState;

	}

	public static void loadGame(Game a) {
		a.update();
	}
